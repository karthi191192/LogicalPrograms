package LogicalPrograms;

public class OddOrEven 
{
    public static void main(String args[])
    {
        int num=9;
        if (num%2==0)
        {
            System.out.println("The given number is Even");
        }
        else
        {
            System.out.println("The given number is Odd");
        }
    } 
}
